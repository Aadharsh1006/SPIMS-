class GroqManager:
    def __init__(self, keys, model_id='llama-3.3-70b-versatile'):
        self.clients = []
        for k in keys:
            if k and not k.startswith('your_secondary_key'):
                try:
                    self.clients.append(Groq(api_key=k))
                    print(f"Groq Client initialized with key: {k[:6]}...{k[-4:]}", flush=True)
                except Exception as e:
                    print(f"Failed to initialize Groq client: {e}", flush=True)
        
        self.current_index = 0
        self.model_id = model_id
        self.logger = logging.getLogger(__name__)

    @property
    def client(self):
        if not self.clients: return None
        return self.clients[self.current_index]

    def rotate(self):
        if len(self.clients) > 1:
            self.current_index = (self.current_index + 1) % len(self.clients)
            self.logger.warning(f"🔄 ROTATING GROQ API KEY -> Slot {self.current_index + 1}")
            print(f"🔄 Rotating Groq API Key to slot {self.current_index + 1}", flush=True)
            return True
        return False

    def call_with_failover(self, method_name, **kwargs):
        """Executes a Groq chat completion with automatic key rotation on failure."""
        if not self.clients: return None
        
        last_error = None
        # Try each client once
        for attempt in range(len(self.clients)):
            client = self.client
            try:
                # Dynamically call the method (usually chat.completions.create)
                if method_name == 'chat.completions.create':
                    # Ensure model is set if not provided
                    if 'model' not in kwargs: kwargs['model'] = self.model_id
                    return client.chat.completions.create(**kwargs)
                elif method_name == 'audio.transcriptions.create':
                    return client.audio.transcriptions.create(**kwargs)
                return None
            except Exception as e:
                last_error = e
                err_msg = str(e).lower()
                self.logger.error(f"Groq Error (Slot {self.current_index + 1}): {err_msg}")
                
                # If it's a rate limit or server error, rotate and retry
                is_retryable = any(x in err_msg for x in ["rate limit", "429", "500", "503", "timeout", "overloaded"])
                if is_retryable and self.rotate():
                    continue
                else:
                    break
        
        self.logger.error(f"Groq Failover Exhausted: {last_error}")
        return None

