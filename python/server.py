def try_ollama_gen(prompt, system_prompt=None):
    """General purpose Ollama generator with JSON support for fallbacks."""
    global OLLAMA_AVAILABLE
    if not OLLAMA_AVAILABLE:
        return None
    try:
        logger.info(f"Ollama Fallback Engine starting for prompt: {prompt[:50]}...")
        messages = []
        if system_prompt:
            messages.append({'role': 'system', 'content': system_prompt})
        messages.append({'role': 'user', 'content': prompt})
        
        response = ollama_client.chat(
            model=OLLAMA_MODEL,
            messages=messages,
            format='json',
            options={'temperature': 0.1}
        )
        text = response.message.content.strip()
        # Robust JSON extraction
        if "```json" in text: text = text.split("```json")[1].split("```")[0].strip()
        elif "```" in text: text = text.split("```")[1].split("```")[0].strip()
        return json.loads(text)
    except Exception as e:
        logger.error(f"Ollama Gen Error: {str(e)}")
        return None

