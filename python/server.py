def parse_with_groq(text):
    if not groq_manager.clients: return None
    try:
        cleaned = clean_text(text)
        prompt = f"{SYSTEM_PROMPT}\n\nResume Text for Extraction:\n{cleaned}"
        print(f"Calling Groq ({MODEL_ID}) for resume parsing...", flush=True)
        
        response = groq_manager.call_with_failover(
            'chat.completions.create',
            messages=[{"role": "user", "content": prompt}],
            temperature=0,
            response_format={"type": "json_object"}
        )

        if not response: return None
        text_out = response.choices[0].message.content
        if not text_out: return None
        res_data = json.loads(text_out)
        
        # Post-process
        array_fields = ["skills", "achievements", "certifications", "softSkills", "education", "experience", "careerPaths"]
        for field in array_fields:
            if field in res_data:
                field_val = res_data.get(field)
                if isinstance(field_val, list):
                    if field in ["skills", "achievements", "certifications", "softSkills", "careerPaths"]:
                        res_data[field] = sorted(list(set([str(item).strip() for item in field_val if item])))
                    else:
                        res_data[field] = [item for item in field_val if item]
                else: res_data[field] = []
            else: res_data[field] = []
        
        try: res_data["atsScore"] = int(res_data.get("atsScore", 0))
        except: res_data["atsScore"] = 0
        try: res_data["profileStrength"] = int(res_data.get("profileStrength", 0))
        except: res_data["profileStrength"] = 0
        return res_data
    except Exception as e:
        print(f"Gemini Error: {e}")
        return None

