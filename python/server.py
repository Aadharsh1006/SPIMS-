@app.route('/api/ai/predict-path', methods=['POST'])
def predict_path():
    try:
        data = request.json or {}
        profile = data.get('profile', {})
        cache_key = get_cache_key("predict-path", profile)
        
        wait_event = None
        with cache_lock:
            if cache_key in AI_CACHE:
                return jsonify(AI_CACHE[cache_key])
            if cache_key in AI_PENDING:
                wait_event = AI_PENDING[cache_key]
            else:
                AI_PENDING[cache_key] = threading.Event()

        if wait_event:
            logger.info("Neural Cache HIT (In-Flight) for predict-path. Waiting...")
            if not wait_event.wait(timeout=45):
                return jsonify({"error": "AI processing timeout"}), 504
            with cache_lock:
                result = AI_CACHE.get(cache_key)
                if result: return jsonify(result)
                return jsonify({"error": "Parallel AI request failed"}), 500

        # We are the Primary executor
        try:
            logger.info(f"Predicting path for profile: {profile.get('skills', [])[:5]}...")
            prompt = f"""
            Analyze this student's professional profile and current placement readiness.
            
            Profile Metrics:
            - Profile Strength (Completeness): {profile.get('profileStrength', 0)}%
            - Technical Skills: {profile.get('skills', [])}
            - Department: {profile.get('department', 'General')}
            
            Based on this, generate a high-fidelity 'Professional Growth Blueprint'. 
            This is NOT just a suggestion; it is a core career strategy.
            
            Provide exactly 3 STRATEGIC PHASES. Each phase must include:
            - 'step': (1, 2, or 3)
            - 'phase': A professional phase name (e.g., 'Phase 1: Foundation & Technical Deep-dive')
            - 'task': A high-level strategic objective.
            - 'detail': A deep, actionable description including specific technical targets or industry standards.
            - 'impact': 'High', 'Medium', or 'Low'.
            - 'duration': Estimated weeks/months (e.g., '4-6 Weeks').
            
            Return ONLY a valid JSON object with the key 'actionPlan'.
            """
            
            ai_data = None
            # Try Groq with Failover
            response = groq_manager.call_with_failover(
                'chat.completions.create',
                messages=[{"role": "user", "content": prompt}],
                temperature=0.1,
                response_format={"type": "json_object"}
            )
            
            if response:
                try:
                    text = response.choices[0].message.content.strip()
                    if "```json" in text: text = text.split("```json")[1].split("```")[0].strip()
                    elif "```" in text: text = text.split("```")[1].split("```")[0].strip()
                    ai_data = json.loads(text)
                except Exception as e:
                    logger.error(f"Failed to parse Groq response: {e}")

            # Fallback to Ollama
            if not ai_data:
                ai_data = try_ollama_gen(prompt)

            if not ai_data:
                return jsonify({"error": "All AI engines failed"}), 503

            with cache_lock:
                AI_CACHE[cache_key] = ai_data
            return jsonify(ai_data)

        finally:
            with cache_lock:
                if cache_key in AI_PENDING:
                    AI_PENDING[cache_key].set()
                    del AI_PENDING[cache_key]

    except Exception as e:
        logger.error(f"Predict Path Final Error: {str(e)}")
        return jsonify({"error": str(e)}), 500

